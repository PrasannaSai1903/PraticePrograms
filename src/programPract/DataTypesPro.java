package programPract;

public class DataTypesPro 
{
	
		public int a=10;
		private int b=20;
		int c=30;
		protected int d = 40;
		
		public void pubMeth()
		{
			System.out.println(a);
		}
		
		private void privMeth()
		{
			System.out.println(b);
		}
		
		void defMeth()
		{
			System.out.println(c);
		}
		
		protected void protMeth()
		{
			System.out.println(d);
		}
	
}

